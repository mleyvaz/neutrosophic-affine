"""
Figure 3: Range-width overestimation vs exact value, per formalism, across 15 expressions.
Uses bench_results.csv generated by benchmarks/bench_all.py
"""
import os, csv
import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np

HERE = os.path.dirname(__file__)
CSV = os.path.join(HERE, "..", "benchmarks", "bench_results.csv")
OUT = os.path.join(HERE, "fig3_benchmark.png")

with open(CSV, encoding="utf-8") as f:
    reader = csv.reader(f)
    rows = list(reader)

header = rows[0]
data = rows[1:]

ids = [r[0] for r in data]
exprs = [r[1] for r in data]
exact = [float(r[3]) for r in data]

def parse_pct(val):
    return float(val) if val != "NA" else np.nan

iv_over = [parse_pct(r[12]) for r in data]
af_over = [parse_pct(r[13]) for r in data]
nt_over = [parse_pct(r[14]) for r in data]
ms_over = [parse_pct(r[15]) for r in data]

# Cap at 1200 % for visual, but annotate real values beyond.
CAP = 500
def cap(vals):
    return [min(v, CAP) if not np.isnan(v) else np.nan for v in vals]

fig, (ax1, ax2) = plt.subplots(2, 1, figsize=(12, 8.5))
fig.suptitle("Benchmark: Range-Width Overestimation per Formalism (15 algebraic expressions)",
             fontsize=14, fontweight='bold')

x = np.arange(len(ids))
width = 0.2

ax1.bar(x - 1.5 * width, cap(iv_over), width, label='Interval', color='#C00000', alpha=0.85)
ax1.bar(x - 0.5 * width, cap(af_over), width, label='Affine',  color='#4472C4', alpha=0.85)
ax1.bar(x + 0.5 * width, cap(nt_over), width, label='Neutrosophic a+bI', color='#70AD47', alpha=0.85)
ax1.bar(x + 1.5 * width, cap(ms_over), width, label='MSNN (multi-source)',   color='#ED7D31', alpha=0.85)

ax1.set_xticks(x)
ax1.set_xticklabels(ids, rotation=0, fontsize=9)
ax1.set_ylabel("Overestimation vs exact range (%)", fontsize=10)
ax1.set_title(f"(a) Overestimation per expression (capped at {CAP}%; real values above cap annotated)",
              fontsize=11)
ax1.legend(loc='upper left', fontsize=9)
ax1.grid(alpha=0.3, axis='y')
ax1.axhline(0, color='black', linewidth=0.6)

# Annotate values that exceeded cap
for i, (iv, af, nt, ms) in enumerate(zip(iv_over, af_over, nt_over, ms_over)):
    for j, (v, c) in enumerate([(iv, '#C00000'), (af, '#4472C4'), (nt, '#70AD47'), (ms, '#ED7D31')]):
        if not np.isnan(v) and v > CAP:
            offset = (j - 1.5) * width
            ax1.annotate(f'{v:.0f}%', xy=(i + offset, CAP), xytext=(i + offset, CAP + 10),
                         ha='center', fontsize=7, color=c, fontweight='bold')

# ---- Second panel: which family of expressions each formalism wins ----
ax2.set_title("(b) Ranking: tighter formalism per expression (exact=0 marked)", fontsize=11)

classes = []
for i in range(len(ids)):
    vals = {
        'Interval': iv_over[i],
        'Affine': af_over[i],
        'Neutrosophic': nt_over[i],
        'MSNN': ms_over[i],
    }
    vals_valid = {k: v for k, v in vals.items() if not np.isnan(v)}
    winner = min(vals_valid, key=vals_valid.get)
    classes.append((winner, vals_valid[winner]))

# Plot which formalism wins per expression
formalisms = ['Interval', 'Affine', 'Neutrosophic', 'MSNN']
colors_map = {'Interval': '#C00000', 'Affine': '#4472C4', 'Neutrosophic': '#70AD47', 'MSNN': '#ED7D31'}

for i, (winner, overest) in enumerate(classes):
    ax2.scatter(i, formalisms.index(winner), s=150, c=colors_map[winner], zorder=3,
                edgecolors='black', linewidth=1)
    label = f"{overest:.0f}%" if overest != 0 else "exact"
    ax2.annotate(label, xy=(i, formalisms.index(winner)), xytext=(0, 10),
                 textcoords='offset points', fontsize=8, ha='center')

ax2.set_xticks(x)
ax2.set_xticklabels(ids, rotation=0, fontsize=9)
ax2.set_yticks(range(4))
ax2.set_yticklabels(formalisms)
ax2.set_ylabel("Winning formalism", fontsize=10)
ax2.grid(alpha=0.3)
ax2.set_xlim(-0.5, len(ids) - 0.5)
ax2.set_ylim(-0.5, 3.5)

plt.tight_layout(rect=[0, 0, 1, 0.96])
plt.savefig(OUT, dpi=150, bbox_inches='tight')
print(f"Saved: {OUT}")
